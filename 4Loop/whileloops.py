i = 1           #I kita deklarasikan nilai awal 1
while i < 5:    # selama variabel nya kurang dari 5, python akan melakukan loop
    print(i)    # variabel nya akan di print, setelah while harus di-tab
    i+=1        #Setelah i di print, python akan menambahkan nilai i sebanyak 1 (2)
                #Python akan mengevaluasi mulai dari line 2, akan menentukan  apakah nilai i baru masih <5